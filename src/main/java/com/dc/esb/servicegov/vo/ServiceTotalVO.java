package com.dc.esb.servicegov.vo;

public class ServiceTotalVO {

	private String firstCategory;
	private String secondCategory;
	private int serviceCount;
	private int operationCount;
	private int serviceTotal;
	private int operationTotal;
	
	public String getFirstCategory() {
		return firstCategory;
	}
	public void setFirstCategory(String firstCategory) {
		this.firstCategory = firstCategory;
	}
	public String getSecondCategory() {
		return secondCategory;
	}
	public void setSecondCategory(String secondCategory) {
		this.secondCategory = secondCategory;
	}
	public int getServiceCount() {
		return serviceCount;
	}
	public void setServiceCount(int serviceCount) {
		this.serviceCount = serviceCount;
	}
	public int getOperationCount() {
		return operationCount;
	}
	public void setOperationCount(int operationCount) {
		this.operationCount = operationCount;
	}
	public int getServiceTotal() {
		return serviceTotal;
	}
	public void setServiceTotal(int serviceTotal) {
		this.serviceTotal = serviceTotal;
	}
	public int getOperationTotal() {
		return operationTotal;
	}
	public void setOperationTotal(int operationTotal) {
		this.operationTotal = operationTotal;
	}
	
}
