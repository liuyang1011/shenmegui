package com.dc.esb.servicegov.vo;

public class TranCountVO {
	private String name;
	private String ptrancount;
	private String ctrancount;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPtrancount() {
		return ptrancount;
	}
	public void setPtrancount(String ptrancount) {
		this.ptrancount = ptrancount;
	}
	public String getCtrancount() {
		return ctrancount;
	}
	public void setCtrancount(String ctrancount) {
		this.ctrancount = ctrancount;
	}
	
}
