package com.dc.esb.servicegov.vo;

public class SystemInvokeServiceInfo {
	private String systemAB;
	private String systemName;
	private String firstPublishDate;
	private String secondPublishDate;
	private String provideServiceNum;
	private String consumeServiceNum;
	private String provideOperationNum;
	private String consumeOperationNum;
	public String getSystemAB() {
		return systemAB;
	}
	public void setSystemAB(String systemAB) {
		this.systemAB = systemAB;
	}
	public String getSystemName() {
		return systemName;
	}
	public void setSystemName(String systemName) {
		this.systemName = systemName;
	}
	public String getProvideServiceNum() {
		return provideServiceNum;
	}
	public void setProvideServiceNum(String provideServiceNum) {
		this.provideServiceNum = provideServiceNum;
	}
	public String getConsumeServiceNum() {
		return consumeServiceNum;
	}
	public void setConsumeServiceNum(String consumeServiceNum) {
		this.consumeServiceNum = consumeServiceNum;
	}
	public String getProvideOperationNum() {
		return provideOperationNum;
	}
	public void setProvideOperationNum(String provideOperationNum) {
		this.provideOperationNum = provideOperationNum;
	}
	public String getConsumeOperationNum() {
		return consumeOperationNum;
	}
	public void setConsumeOperationNum(String consumeOperationNum) {
		this.consumeOperationNum = consumeOperationNum;
	}
	public String getFirstPublishDate() {
		return firstPublishDate;
	}
	public void setFirstPublishDate(String firstPublishDate) {
		this.firstPublishDate = firstPublishDate;
	}
	public String getSecondPublishDate() {
		return secondPublishDate;
	}
	public void setSecondPublishDate(String secondPublishDate) {
		this.secondPublishDate = secondPublishDate;
	}
	
	

}
